    public frmAlumnos() {
        initComponents();
        
        setLocationRelativeTo(null);
        setTitle("Ejercicio Herencia");
        
         int edad, clave,prom,semestre,i=0;
        String nombre,tipo="",carrera;
        File datos;
        Scanner lee;
        datos = new File("Datos.txt");
        try
        {
            lee = new Scanner(datos);
        }
        catch(Exception e)
        {
            lee = null;
            System.out.println("Archivo no encontrado");
        }

       if (lee != null)
        {
            while(lee.hasNextInt()) //regresa true solo sisigue un int 
            {
                clave = lee.nextInt();
                edad = lee.nextInt();
                lee.nextLine();// esto va a ahcer que podamos leer un string si acabas de leer un int 
                nombre = lee.nextLine();
                tipo = lee.nextLine();
                tipo = tipo.toUpperCase();
                carrera = lee.nextLine();
                    prom = lee.nextInt();
                    semestre = lee.nextInt();
                if (tipo.equals("ALUMNO"))
                {
                    vectorObjetosA[i] = new Alumno(clave, edad, nombre, tipo, carrera, prom, semestre);
                    cmbListaAlumnos.addItem(nombre);
                    System.out.println(vectorObjetosA[i].toString());
                    i++;
                    
                }
            }
            lee.close();
            c++;
        }
    } 
    
